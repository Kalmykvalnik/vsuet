def sum_of_cubes():
    """
    Запрашивает у пользователя натуральное число n и вычисляет сумму кубов чисел от 1 до n.
    """
    while True:
        try:
            n = int(input("Введите натуральное число n: "))
            if n > 0:
                break  # Выход из цикла, если n > 0
            else:
                print(
                    "Ошибка: n должно быть натуральным числом (больше 0). Попробуйте еще раз."
                )
        except ValueError:
            print(
                "Ошибка: Введены некорректные данные. Пожалуйста, введите натуральное число."
            )

    total_sum = 0  # Инициализация суммы
    for i in range(1, n + 1):  # range до n+1, так как n должно включаться
        total_sum += i**3  # Добавляем куб текущего числа к сумме

    print("Сумма кубов от 1 до", n, "равна:", total_sum)


# Вызов функции
sum_of_cubes()
