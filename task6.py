def factorial():
    """
    Запрашивает у пользователя натуральное число n и вычисляет его факториал (n!).
    Не использует математическую библиотеку math.
    """

    while True:
        try:
            n = int(input("Введите натуральное число n: "))
            if n > 0:
                break  # Выход из цикла, если n > 0
            else:
                print(
                    "Ошибка: n должно быть натуральным числом (больше 0). Попробуйте еще раз."
                )
        except ValueError:
            print(
                "Ошибка: Введены некорректные данные. Пожалуйста, введите натуральное число."
            )

    result = 1  # Инициализируем факториал единицей
    for i in range(1, n + 1):  # range до n+1, тк n должен включаться
        result *= i  # Умножаем текущий результат на число i

    print("Факториал числа", n, "равен:", result)


# Вызов функции
factorial()
