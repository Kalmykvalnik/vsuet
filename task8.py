def print_staircase():
    """
    Запрашивает у пользователя натуральное число n (n <= 9) и выводит
    лесенку из n ступенек, где i-я ступенька состоит из чисел от 1 до i без пробелов.
    """

    while True:
        try:
            n = int(input("Введите натуральное число n (n <= 9): "))
            if 1 <= n <= 9:
                break  # Выход из цикла, если 1 <= n <= 9
            else:
                print(
                    "Ошибка: n должно быть натуральным числом от 1 до 9. Попробуйте еще раз."
                )
        except ValueError:
            print(
                "Ошибка: Введены некорректные данные. Пожалуйста, введите натуральное число."
            )

    for i in range(1, n + 1):  # Цикл для каждой ступеньки
        for j in range(1, i + 1):  # Цикл для чисел внутри ступеньки
            print(j, end="")  # Выводим число без пробела в конце
        print()  # Переход на новую строку после вывода ступеньки


# Вызов функции
print_staircase()
