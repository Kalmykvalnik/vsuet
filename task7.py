def sum_of_factorials():
    """
    Запрашивает у пользователя натуральное число n и вычисляет сумму факториалов
    от 1! до n!, используя только один цикл.
    Не использует математическую библиотеку math.
    """

    while True:
        try:
            n = int(input("Введите натуральное число n: "))
            if n > 0:
                break
            else:
                print(
                    "Ошибка: n должно быть натуральным числом (больше 0). Попробуйте еще раз."
                )
        except ValueError:
            print(
                "Ошибка: Введены некорректные данные. Пожалуйста, введите натуральное число."
            )

    total_sum = 0  # Инициализируем сумму факториалов
    current_factorial = 1  # Инициализируем значение текущего факториала

    for i in range(1, n + 1):  # range до n+1, тк n должно включаться
        current_factorial *= i  # Вычисляем факториал i
        total_sum += current_factorial  # Добавляем текущий факториал к общей сумме

    print("Сумма факториалов от 1! до", n, "! равна:", total_sum)


# Вызов функции
sum_of_factorials()
