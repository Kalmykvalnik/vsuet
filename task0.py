def fibonacci_sum_with_start():
    """
    Запрашивает у пользователя два числа: N (количество чисел Фибоначчи) и K (порядковый номер
    начала) и вычисляет сумму N чисел Фибоначчи, начиная с K-го.
    Используется только один цикл.
    """

    while True:
        try:
            N = int(input("Введите количество чисел N из ряда Фибоначчи: "))
            K = int(input("Введите порядковый номер начала K: "))
            if N >= 0 and K >= 1:
                break
            else:
                print(
                    "Ошибка: N должно быть неотрицательным, а K должно быть натуральным числом. Попробуйте еще раз."
                )
        except ValueError:
            print(
                "Ошибка: Введены некорректные данные. Пожалуйста, введите целые числа."
            )

    if N == 0:
        print("Сумма чисел Фибоначчи равна: 0")
        return

    a = 0
    b = 1
    total_sum = 0

    for i in range(K - 1 + N):  # проходим до N чисел начиная с K-1
        if i >= K - 1:
            total_sum += b
        c = a + b
        a = b
        b = c

    print("Сумма", N, "чисел Фибоначчи, начиная с", K, "-го номера, равна:", total_sum)


# Вызов функции
fibonacci_sum_with_start()
