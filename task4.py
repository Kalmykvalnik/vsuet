def sum_of_numbers():
    """
    Считывает количество чисел M, затем считывает M целых чисел и вычисляет их сумму.
    Используется минимальное количество переменных.
    """
    while True:
        try:
            M = int(input("Введите количество чисел (M): "))
            if M > 0:
                break  # Выход из цикла, если M > 0
            else:
                print("Ошибка: M должно быть положительным числом. Попробуйте еще раз.")
        except ValueError:
            print(
                "Ошибка: Введены некорректные данные. Пожалуйста, введите целое положительное число."
            )

    total_sum = 0  # Инициализируем сумму нулем

    for _ in range(
        M
    ):  # "_" используется когда переменная не важна, только количество итераций
        while True:
            try:
                num = int(input("Введите целое число: "))
                total_sum += num  # Добавляем введенное число к сумме
                break  # Выход из внутреннего цикла, если ввод корректен
            except ValueError:
                print(
                    "Ошибка: Введены некорректные данные. Пожалуйста, введите целое число."
                )

    print("Сумма введенных чисел:", total_sum)


# Вызов функции
sum_of_numbers()
